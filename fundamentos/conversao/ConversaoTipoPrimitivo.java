package fundamentos.conversao;

public class ConversaoTipoPrimitivo {

    public static void main(String[] args) {

        double a = 1;
        System.out.println(a);

        float b = (float) 1.22342353225342;
        System.out.println(b);

        int c = 340;
        byte d = (byte) c;
        System.out.println(d);
    }
}
